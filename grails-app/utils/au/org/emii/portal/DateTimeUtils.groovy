package au.org.emii.portal

class DateTimeUtils {
    final static int MILLISECOND = 1 // The base unit
    final static int SECOND      = 1000 * MILLISECOND
    final static int MINUTE      = 60 * SECOND
    final static int HOUR        = 60 * MINUTE
    final static int DAY         = 24 * HOUR

    final static int MILLISECONDS = MILLISECOND
    final static int SECONDS      = SECOND
    final static int MINUTES      = MINUTE
    final static int HOURS        = HOUR
    final static int DAYS         = DAY
}
